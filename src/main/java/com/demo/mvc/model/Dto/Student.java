package com.demo.mvc.model.Dto;

import java.util.ArrayList;
import java.util.List;

public class Student {
    private String id;            // 主键（自增）
    private String studentName; // 姓名
    private String sexType;        // 性别 0:女 1：男
    private List<SexInfo> sexList = new ArrayList<>();        // 性别 0:女 1：男
    private String seqNo;       // 学号
    private String address;     // 家庭住址
    private String birthday;    // 生日
    private String timeOfEnrollment ; // 入学时间
    private String  dateOFGraduation;  // 毕业时间

    public String getSexType() {
        return sexType;
    }

    public void setSexType(String sexType) {
        this.sexType = sexType;
    }

    public List<SexInfo> getSexList() {
        return sexList;
    }

    public void setSexList(List<SexInfo> sexList) {
        this.sexList = sexList;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getSeqNo() {
        return seqNo;
    }

    public void setSeqNo(String seqNo) {
        this.seqNo = seqNo;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getTimeOfEnrollment() {
        return timeOfEnrollment;
    }

    public void setTimeOfEnrollment(String timeOfEnrollment) {
        this.timeOfEnrollment = timeOfEnrollment;
    }

    public String getDateOFGraduation() {
        return dateOFGraduation;
    }

    public void setDateOFGraduation(String dateOFGraduation) {
        this.dateOFGraduation = dateOFGraduation;
    }
}
